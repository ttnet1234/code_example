def write_cat():
    print("/_/\\") 
    print("( o.o )") 
    print("> ^ <")
    print("--meow---")
    print("meow")


def draw_ascii_dog():
    """
    Generates and prints an ASCII representation of a dog.
    """
    dog = """
     /\_/\
    ( o.o )
     >" "<
         ||
         ==
    """
    print(dog)


def draw_a_house():
    """
    Generates and prints an ASCII representation of a house.
    """
    house = """
     _ _ _ _ _ _
    |         |
    |         |
    |         |
    |         |
    |         |
    |         |
    |         |
    |         |
    |         |
    """
    print(house)

if __name__ == "__main__":
    print("meow")
    write_cat() 
    draw_ascii_dog()
    print("meow")
    draw_a_house()

